/*
Comparison Operators Examples
==	Equal to	x == y	
!=	Not equal	x != y	
>	Greater than	x > y	
<	Less than	x < y	
>=	Greater than or equal to	x >= y	
<=	Less than or equal to	x <= y

*/

public class Comp {

  public static void main(String[] args) {
      int a=7,b=8;

      System.out.println(a>=b);
      System.out.println(a<=b);
      System.out.println(a==b);
      System.out.println(a!=b);
      System.out.println(a>b);
      System.out.println(a<b);


  }
}
